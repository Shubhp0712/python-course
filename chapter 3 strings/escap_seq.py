st1 = "Hy i am shubh and i am a \"programmer\"." # this escape sequence is used for the double quotes
print(st1)

st2 = "Hy i am shubh \nI am a programmer." # this escape sequence is used for the new line
print(st2)

st3 = "Hy i am shubh \t i am a programmer." # this escape sequence is used for the tab using number of spaces
print(st3)