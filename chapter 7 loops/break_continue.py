for i in range(1,51):
    if(i == 22):
        break # This will break the loop when the value of i is 22.
    print(i)

# Break statement is used to break or terminate the loop when the condition is satisfied.


for i in range(1,51):
    if(i == 22):
        continue  # This will skip the value of i when it is 22.
    print(i)

# Continue statement is used to skip the value of i when the condition is satisfied.