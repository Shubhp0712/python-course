# We have to print the numbers from 1 to 100

for i in range(1,101): # This loop will run from 1 to 100.
    print(i)

# Here the simple for llop concept is used in above code snippet.

# Now in for loop we can use range function in some other way also.

for i in range (1,31,3):  # This loop will run from 1 to 30 and the increment will be 3 or jump to 1 to 4 and so on.
    print(i)